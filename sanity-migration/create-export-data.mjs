#!/usr/bin/env node
/**
 * EXPORT DATA HELPER
 * 
 * Questo script va eseguito PRIMA dell'import.
 * Legge projects.ts e posts.ts e crea un file JSON pulito.
 * 
 * Uso:
 *   node create-export-data.mjs
 * 
 * Crea: export-data.mjs (usato da import-to-sanity.mjs)
 */

import { readFileSync, writeFileSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));

// Percorso al progetto — ADATTA SE DIVERSO
const SITE_DIR = join(__dirname, '..');

function parseProjectsTs() {
  let content = readFileSync(join(SITE_DIR, 'src/data/projects.ts'), 'utf-8');
  
  // Remove TypeScript types and exports
  content = content
    .replace(/export type [\s\S]*?;\n/g, '')
    .replace(/export interface [\s\S]*?}\n/g, '')
    .replace(/export const projects: Project\[\] = /, 'const projects = ')
    .replace(/export const categories[\s\S]*$/, '')
    .replace(/export function[\s\S]*$/, '');
  
  // Evaluate safely
  const fn = new Function(`${content}; return projects;`);
  return fn();
}

function parsePostsTs() {
  let content = readFileSync(join(SITE_DIR, 'src/data/posts.ts'), 'utf-8');
  
  content = content
    .replace(/export interface [\s\S]*?}\n/g, '')
    .replace(/export const posts: BlogPost\[\] = /, 'const posts = ')
    .replace(/export function[\s\S]*$/g, '');
  
  const fn = new Function(`${content}; return posts;`);
  return fn();
}

try {
  const projects = parseProjectsTs();
  const posts = parsePostsTs();
  
  console.log(`✅ Parsed ${projects.length} projects`);
  console.log(`✅ Parsed ${posts.length} blog posts`);
  
  // Write as ES module
  const output = `// Auto-generated by create-export-data.mjs — DO NOT EDIT
export const projects = ${JSON.stringify(projects, null, 2)};
export const posts = ${JSON.stringify(posts, null, 2)};
`;
  
  writeFileSync(join(__dirname, 'export-data.mjs'), output);
  console.log(`\n📄 export-data.mjs created successfully`);
  console.log(`\nNext: SANITY_TOKEN=<your-token> node import-to-sanity.mjs`);
} catch (err) {
  console.error('❌ Error:', err.message);
  console.error('\nMake sure the site project is at:', SITE_DIR);
}
